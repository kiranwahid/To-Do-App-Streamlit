# Write a Python program that takes two integer inputs from the user and calculates their sum. 
# The program should perform the following tasks:

#  =============== Answer ===============

# 1. Prompt the user to enter the first integer
# 2. Prompt the user to enter the second integer
# 3. Calculate the sum of the two integers
# 4. Display the sum to the user

num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Calculate the sum
sum = num1 + num2

# Display the sum
print(f"The sum of {num1} and  {num2} is {sum}")